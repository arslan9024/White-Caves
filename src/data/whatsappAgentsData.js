export const getAgentStatusCounts = () => {
  const counts = {
    active: 0,
    blocked: 0,
    submitted: 0,
    permanent_blocked: 0
  };
  
  WHATSAPP_AGENTS.forEach(agent => {
    counts[agent.whatsappStatus]++;
  });
  
  return counts;
};

export const getActiveAgents = () => {
  return WHATSAPP_AGENTS.filter(agent => agent.whatsappStatus === 'active');
};

export const getBlockedAgents = () => {
  return WHATSAPP_AGENTS.filter(agent => 
    agent.whatsappStatus === 'blocked' || agent.whatsappStatus === 'permanent_blocked'
  );
};

export const getTopPerformers = () => {
  return WHATSAPP_AGENTS
    .filter(agent => agent.whatsappStatus === 'active')
    .sort((a, b) => b.performance.conversionRate - a.performance.conversionRate);
};

